

import Foundation
import UIKit

extension UIFont {
  static func nanumGothic(size: CGFloat) -> UIFont {
    return UIFont(name: "NanumBarunGothic", size: size)!
  }
  
  static func nanumGothicBold(size: CGFloat) -> UIFont {
    return UIFont(name: "NanumBarunGothicBold", size: size)!
  }
  
  static func nanumGothicLight(size: CGFloat) -> UIFont {
    return UIFont(name: "NanumBarunGothicLight", size: size)!
  }
  
  static func nanumGothicUltraLight(size: CGFloat) -> UIFont {
    return UIFont(name: "NanumBarunGothicUltraLight", size: size)!
  }
}
